size_t pointer_size = 0;


//------------------------------------------------------------------------------
// 포인터 사이즈 반환.
//------------------------------------------------------------------------------
size_t pointer_size_of() {
	if (pointer_size == 0)
		pointer_size = sizeof(void*);
	return pointer_size;
}



